#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	comp;

	comp = 0;
	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1 [i] < s2 [i] || s1 [i] > s2 [i])
		{
			comp = s1[i] - s2[i];
			return (comp);
		}
		i++;
	}
	return (comp);
}
